public class Class {
    // Instance variables
    private String className; // Name of the class
    private String requiredGrade; // Required grade to pass

    // Constructor
    public Class(String className) {
        this.className = className;
        this.requiredGrade = requiredGrade;
    }

    // Getter for className
    public String getClassName() {
        return className;
    }

    // Getter for requiredGrade
    public String requiredGrade() {
        return requiredGrade;
    }

    // Setter for className
    public void setClassName(String className) {
        this.className = className;
    }

    // Setter for requiredGrade
    public void setRequiredGrade(String requiredGrade) {
        this.requiredGrade = requiredGrade;
    }
}
